/**
 * Relation Operators are used for comparisions few of them are { < , > , <=, >= , == }
 * 
**/

#include <iostream>
using namespace std;

int main()
{
    int a = 1, b = 3;
    cout << (a == b) << endl;
    cout << (a <= b) << endl;
    cout << (a >= b) << endl;
    cout << (a < b) << endl;
    cout << (a > b) << endl;

    return 0;
}
